package hw11a10a2022;

public class Season {
    public static void main(String[] args) {
        int num = 2;
        switch (num){
            case 1:
                System.out.println("Winter");
                break;
            case 2:
                System.out.println("Spring");
                break;
            case 3:
                System.out.println("Summer");
                break;
            case 4:
                System.out.println("Autumn");
                break;
            default:
                System.out.println("Error");
        }
    }
}
